/**
 * Ember theme. Color is the only thing Material You touches — typography/shape/spacing are always
 * Ember. Edge-to-edge / system-bar styling is the app's responsibility (FR-ADPT-3), not the
 * theme's.
 *
 * @param darkTheme follow the system by default; wire to the Settings theme selector.
 * @param dynamicColor Material You wallpaper colors on Android 12+; when true the Ember brand
 *   [ColorScheme] is replaced by the device-generated scheme (extended brand colors stay constant).
 */
@Composable
fun EmberTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    dynamicColor: Boolean = true,
    content: @Composable () -> Unit,
) {
    val colorScheme =
        when {
            dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
                val context = LocalContext.current
                if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
            }
            darkTheme -> DarkColors
            else -> LightColors
        }
    val extendedColors = if (darkTheme) EmberExtendedColorsDark else EmberExtendedColorsLight

    CompositionLocalProvider(
        LocalEmberExtendedColors provides extendedColors,
        LocalEmberSpacing provides EmberSpacing,
    ) {
        MaterialTheme(
            colorScheme = colorScheme,
            typography = EmberTypography,
            shapes = EmberShapes,
            content = content,
        )
    }
}
